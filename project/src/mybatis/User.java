public class User {
	
	private String username ;
	private String password ;
	private int level ;
	private int grade ;
	private int time ;
	private int maxgrard ;
	private int blood ;
	
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public int getMaxgrard() {
		return maxgrard;
	}
	public void setMaxgrard(int maxgrard) {
		this.maxgrard = maxgrard;
	}
	public int getBlood() {
		return blood;
	}
	public void setBlood(int blood) {
		this.blood = blood;
	}

}
  

